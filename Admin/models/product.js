function Product(name,img,price,screen,backCamera,frontCamera,desc,type,quantity){
    this.name=name;
    this.img=img;
    this.price=price;
    this.screen=screen;
    this.backCamera=backCamera;
    this.frontCamera=frontCamera;
    this.desc=desc;
    this.type=type;
    this.quantity=quantity;
}